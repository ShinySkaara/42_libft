unsigned int	ft_byteswap32(unsigned int x)
{
	unsigned int	a;
	unsigned int	b;

	a = x;
	b = (x >> 16);
	a = (a >> 8) | (a << 8);
	b = (b >> 8) | (b << 8);	
	return ((a << 16) | b);
}
